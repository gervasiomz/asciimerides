def is_leap_year(year: int) -> bool:
    return year % 4 == 0 and (year % 100 != 0 or year % 400 == 0)


def main():
    today = datetime.now()
    month = today.strftime("%m")
    day = today.strftime("%d")

    # Día del programador: 13 sep (normal) o 12 sep (bisiesto)
    if month == "09":
        key = ("09", "12") if is_leap_year(today.year) else ("09", "13")
    else:
        key = (month, day)

    if key in EPHEMERIS:
        title, art = EPHEMERIS[key]
        print()
        print(art.strip())
        print(f"   >>> {title} — {today.strftime('%d/%m/%Y')} <<<")
        print()
    else:
        print()
        print(fr"""
     _________________________
    < ¡Buen día! ☕  {today.strftime('%d/%m/%Y')}  >
     -------------------------
            \   ^__^
             \  (oo)\_______
                (__)\       )\/\
                    ||----w |
                    ||     ||
""")
        print()


if __name__ == "__main__":
    main()
